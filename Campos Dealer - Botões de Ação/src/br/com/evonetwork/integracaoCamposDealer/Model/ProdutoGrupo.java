package br.com.evonetwork.integracaoCamposDealer.Model;

public class ProdutoGrupo {
	private int idProdutoGrupo;
	private String codProdutoGrupo;
	private String dscProdutoGrupo;
	private String dthRegistro;

	public int getIdProdutoGrupo() {
		return idProdutoGrupo;
	}

	public void setIdProdutoGrupo(int idProdutoGrupo) {
		this.idProdutoGrupo = idProdutoGrupo;
	}

	public String getCodProdutoGrupo() {
		return codProdutoGrupo;
	}

	public void setCodProdutoGrupo(String codProdutoGrupo) {
		this.codProdutoGrupo = codProdutoGrupo;
	}

	public String getDscProdutoGrupo() {
		return dscProdutoGrupo;
	}

	public void setDscProdutoGrupo(String dscProdutoGrupo) {
		this.dscProdutoGrupo = dscProdutoGrupo;
	}

	public String getDthRegistro() {
		return dthRegistro;
	}

	public void setDthRegistro(String dthRegistro) {
		this.dthRegistro = dthRegistro;
	}

}
