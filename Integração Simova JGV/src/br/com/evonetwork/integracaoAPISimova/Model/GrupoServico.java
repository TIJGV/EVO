package br.com.evonetwork.integracaoAPISimova.Model;

public class GrupoServico {
	
	private String ativo = null;
	private String codGrupoServico = null;
	private String descGrupoServico = null;
	private String codMarca = null;
	
	public String getAtivo() {
		return ativo;
	}
	public void setAtivo(String ativo) {
		this.ativo = ativo;
	}
	public String getCodGrupoServico() {
		return codGrupoServico;
	}
	public void setCodGrupoServico(String codGrupoServico) {
		this.codGrupoServico = codGrupoServico;
	}
	public String getDescGrupoServico() {
		return descGrupoServico;
	}
	public void setDescGrupoServico(String descGrupoServico) {
		this.descGrupoServico = descGrupoServico;
	}
	public String getCodMarca() {
		return codMarca;
	}
	public void setCodMarca(String codMarca) {
		this.codMarca = codMarca;
	}
	
	
}
