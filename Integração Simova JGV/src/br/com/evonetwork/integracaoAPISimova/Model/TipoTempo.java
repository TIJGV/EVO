package br.com.evonetwork.integracaoAPISimova.Model;

public class TipoTempo {
	
	private String ativo = null;
	private String codTipoTempo = null;
	private String descTipoTempo = null;
	
	public String getAtivo() {
		return ativo;
	}
	public void setAtivo(String ativo) {
		this.ativo = ativo;
	}
	public String getCodTipoTempo() {
		return codTipoTempo;
	}
	public void setCodTipoTempo(String codTipoTempo) {
		this.codTipoTempo = codTipoTempo;
	}
	public String getDescTipoTempo() {
		return descTipoTempo;
	}
	public void setDescTipoTempo(String descTipoTempo) {
		this.descTipoTempo = descTipoTempo;
	}
	
	
}
