package br.com.evonetwork.integracaoAPISimova.Model;

public class Equipamento {
	
	private String ativo = null;
	private String chassiVeiculo = null;
	private String chassiReduzido = null;
	private String descVeiculo = null;
	private String codCliente = null;
	private String lojaCliente = null;
	private String codModeloVeiculo = null;
	
	public String getAtivo() {
		return ativo;
	}
	public void setAtivo(String ativo) {
		this.ativo = ativo;
	}
	public String getChassiVeiculo() {
		return chassiVeiculo;
	}
	public void setChassiVeiculo(String chassiVeiculo) {
		this.chassiVeiculo = chassiVeiculo;
	}
	public String getChassiReduzido() {
		return chassiReduzido;
	}
	public void setChassiReduzido(String chassiReduzido) {
		this.chassiReduzido = chassiReduzido;
	}
	public String getDescVeiculo() {
		return descVeiculo;
	}
	public void setDescVeiculo(String descVeiculo) {
		this.descVeiculo = descVeiculo;
	}
	public String getCodCliente() {
		return codCliente;
	}
	public void setCodCliente(String codCliente) {
		this.codCliente = codCliente;
	}
	public String getLojaCliente() {
		return lojaCliente;
	}
	public void setLojaCliente(String lojaCliente) {
		this.lojaCliente = lojaCliente;
	}
	public String getCodModeloVeiculo() {
		return codModeloVeiculo;
	}
	public void setCodModeloVeiculo(String codModeloVeiculo) {
		this.codModeloVeiculo = codModeloVeiculo;
	}
	
}
