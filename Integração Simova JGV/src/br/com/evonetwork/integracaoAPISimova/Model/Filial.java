package br.com.evonetwork.integracaoAPISimova.Model;

public class Filial {
	
	private String filial = null;
	private String nome = null;
	private String descricaoFilial = null;
	private String urlAcesso = null;
	private String filialERP = null;
	private String local = null;
	
	public String getFilial() {
		return filial;
	}
	public void setFilial(String filial) {
		this.filial = filial;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDescricaoFilial() {
		return descricaoFilial;
	}
	public void setDescricaoFilial(String descricaoFilial) {
		this.descricaoFilial = descricaoFilial;
	}
	public String getUrlAcesso() {
		return urlAcesso;
	}
	public void setUrlAcesso(String urlAcesso) {
		this.urlAcesso = urlAcesso;
	}
	public String getFilialERP() {
		return filialERP;
	}
	public void setFilialERP(String filialERP) {
		this.filialERP = filialERP;
	}
	public String getlocal() {
		return local;
	}
	public void setlocal(String local) {
		this.local = local;
	}
}
