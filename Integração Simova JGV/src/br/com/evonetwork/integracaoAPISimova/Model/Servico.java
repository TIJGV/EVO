package br.com.evonetwork.integracaoAPISimova.Model;

public class Servico {
	
	private String ativo = null;
	private String codServico = null;
	private String descServico = null;
	private String tempoFabrica = null;
	private String codGrupoServico = null;
	private String codMarca = null;
	
	public String getAtivo() {
		return ativo;
	}
	public void setAtivo(String ativo) {
		this.ativo = ativo;
	}
	public String getCodServico() {
		return codServico;
	}
	public void setCodServico(String codServico) {
		this.codServico = codServico;
	}
	public String getDescServico() {
		return descServico;
	}
	public void setDescServico(String descServico) {
		this.descServico = descServico;
	}
	public String getTempoFabrica() {
		return tempoFabrica;
	}
	public void setTempoFabrica(String tempoFabrica) {
		this.tempoFabrica = tempoFabrica;
	}
	public String getCodGrupoServico() {
		return codGrupoServico;
	}
	public void setCodGrupoServico(String codGrupoServico) {
		this.codGrupoServico = codGrupoServico;
	}
	public String getCodMarca() {
		return codMarca;
	}
	public void setCodMarca(String codMarca) {
		this.codMarca = codMarca;
	}
	
}
