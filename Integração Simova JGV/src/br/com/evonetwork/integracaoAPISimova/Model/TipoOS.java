package br.com.evonetwork.integracaoAPISimova.Model;

public class TipoOS {
	
	private String ativo = null;
	private String codTipoOS = null;
	private String descTipoOS = null;
	private String atendimento = null;
	
	public String getAtivo() {
		return ativo;
	}
	public void setAtivo(String ativo) {
		this.ativo = ativo;
	}
	public String getCodTipoOS() {
		return codTipoOS;
	}
	public void setCodTipoOS(String codTipoOS) {
		this.codTipoOS = codTipoOS;
	}
	public String getDescTipoOS() {
		return descTipoOS;
	}
	public void setDescTipoOS(String descTipoOS) {
		this.descTipoOS = descTipoOS;
	}
	public String getAtendimento() {
		return atendimento;
	}
	public void setAtendimento(String atendimento) {
		this.atendimento = atendimento;
	}
	
}
